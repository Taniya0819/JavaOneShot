package Basics;


public class dataType {
    public static void main(String[] args) {
        double x =3.1417;
        System.out.println(x);
        System.out.println(3.523);
        System.out.println("3.435");
        // float y = 3.1417;  (error)
        float y = 3.1417f;
        System.out.println(y);
    }
}

